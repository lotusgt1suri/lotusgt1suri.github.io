              <p class="para"><b>Transformer</b></p>
              <p class="para">
              A transformer is an electrical device that transfers energy between two or more circuits t
              hrough electromagnetic induction.
              <br>
              <br>
              A varying current in the transformer's primary winding creates a varying magnetic flux in 
              the core and a varying magnetic field impinging on the secondary winding. This varying mag
              netic field at the secondary induces a varying electromotive force (EMF) or voltage in the 
              secondary winding. Making use of Faraday's Law in conjunction with high magnetic permeabil
              ity core properties, transformers can thus be designed to efficiently change AC voltages f
              rom one voltage level to another within power networks.
              </p>
